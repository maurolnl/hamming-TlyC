    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

            try {
                FileReader fr = new FileReader("fecha.txt");
                BufferedReader br = new BufferedReader(fr);
                String fe = br.readLine();
                DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                Date dia = df.parse(fe);
                Date hoy = new Date();
                if(dia.compareTo(hoy)==-1 || dia.compareTo(hoy)== 0){
                    new ProteccionEncriptacion().descomprimir(Inicio.ruta);
                    JOptionPane.showMessageDialog(null, "descomprimido!");
                    Inicio i = new Inicio();
                    i.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Aun no es el momento...");
                    Inicio i = new Inicio();
                    i.setVisible(true);
                }
            } catch (Exception ex) {
                
            }
        this.setVisible(false);
    }//GEN-LAST:event_jButton4ActionPerformed

